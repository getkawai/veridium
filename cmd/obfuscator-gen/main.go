package main

import (
	"bufio"
	"fmt"
	"log"
	"os"
	"strings"

	"github.com/kawai-network/veridium/pkg/obfuscator"
)

func main() {
	envFile := ".env"
	outputFile := "internal/constant/cloudflare.go"

	file, err := os.Open(envFile)
	if err != nil {
		log.Fatalf("failed to open %s: %v", envFile, err)
	}
	defer file.Close()

	scanner := bufio.NewScanner(file)
	lineNum := 0
	configs := make(map[string]string)

	for scanner.Scan() {
		lineNum++
		// Only process lines 35-40
		if lineNum < 35 {
			continue
		}
		if lineNum > 40 {
			break
		}

		line := strings.TrimSpace(scanner.Text())
		if line == "" || strings.HasPrefix(line, "#") {
			continue
		}

		parts := strings.SplitN(line, "=", 2)
		if len(parts) == 2 {
			key := strings.TrimSpace(parts[0])
			value := strings.TrimSpace(parts[1])
			configs[key] = value
		}
	}

	if err := scanner.Err(); err != nil {
		log.Fatalf("error reading %s: %v", envFile, err)
	}

	// Generate the Go file
	var sb strings.Builder
	sb.WriteString("// Code generated by obfuscator-gen. DO NOT EDIT.\n")
	sb.WriteString("package constant\n\n")
	sb.WriteString("import \"github.com/kawai-network/veridium/pkg/obfuscator\"\n\n")

	sb.WriteString("const (\n")
	for key, value := range configs {
		encoded := obfuscator.EncodeString(value)
		sb.WriteString(fmt.Sprintf("\tobfuscated%s = \"%s\"\n", toCamelCase(key), encoded))
	}
	sb.WriteString(")\n\n")

	for key := range configs {
		camelKey := toCamelCase(key)
		sb.WriteString(fmt.Sprintf("// Get%s returns the decoded value of %s\n", camelKey, key))
		sb.WriteString(fmt.Sprintf("func Get%s() string {\n", camelKey))
		sb.WriteString(fmt.Sprintf("\tval, _ := obfuscator.DecodeString(obfuscated%s)\n", camelKey))
		sb.WriteString("\treturn val\n")
		sb.WriteString("}\n\n")
	}

	err = os.WriteFile(outputFile, []byte(sb.String()), 0644)
	if err != nil {
		log.Fatalf("failed to write %s: %v", outputFile, err)
	}

	fmt.Printf("Successfully generated %s from %s lines 35-40\n", outputFile, envFile)
}

func toCamelCase(s string) string {
	parts := strings.Split(s, "_")
	for i := range parts {
		if len(parts[i]) > 0 {
			parts[i] = strings.ToUpper(parts[i][:1]) + strings.ToLower(parts[i][1:])
		}
	}
	// Special handling for acronyms if needed, e.g. CF -> Cf or CF
	// For now, simple TitleCase
	return strings.Join(parts, "")
}
