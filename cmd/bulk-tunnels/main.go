package main

import (
	"flag"
	"fmt"
	"log"
	"os"
	"path/filepath"
	"strings"
	"time"

	"github.com/kawai-network/veridium/internal/constant"
	"github.com/kawai-network/veridium/pkg/tunnelkit"
	"github.com/kawai-network/x/env"
)

const (
	defaultDomain      = "getkawai.com" // Default domain, can be changed
	defaultTunnelCount = 1
	defaultOutputFile  = "pkg/tunnelkit/tunnels.go"
	defaultPackageName = "tunnelkit"
)

func main() {
	// Although user said "other flags not needed", keeping them as optional overrides
	// is good practice, but defaulting to the requested constants.
	accountID := constant.GetCfAccountId()
	apiToken := constant.GetCfApiToken()
	domain := flag.String("domain", defaultDomain, "Base domain for hostnames")
	count := flag.Int("count", defaultTunnelCount, "Number of tunnels to create")
	output := flag.String("out", defaultOutputFile, "Output Go file path")
	packageName := flag.String("pkg", defaultPackageName, "Package name for generated Go file")

	flag.Parse()

	fmt.Printf("Starting creation of %d tunnels for domain %s...\n", *count, *domain)

	var tunnels []*tunnelkit.TunnelInfo

	// Initialize obfuscator for TunnelToken
	obf := env.New()

	// Create output directory if it doesn't exist
	outDir := filepath.Dir(*output)
	if outDir != "." {
		if err := os.MkdirAll(outDir, 0755); err != nil {
			log.Fatalf("Failed to create output directory: %v", err)
		}
	}

	for i := 0; i < *count; i++ {
		tunnelName := fmt.Sprintf("node-%d", i+1)
		hostname := fmt.Sprintf("%s.%s", tunnelName, *domain)

		fmt.Printf("[%d/%d] Creating tunnel '%s' (Host: %s)... ", i+1, *count, tunnelName, hostname)

		cfg := tunnelkit.Config{
			AccountID:  accountID,
			APIToken:   apiToken,
			TunnelName: tunnelName,
			Hostname:   hostname,
			LocalURL:   constant.LocalContributorURL,
		}

		// Use the refactored function
		info, err := tunnelkit.GetOrCreateTunnelWithDNS(cfg)
		if err != nil {
			fmt.Printf("FAILED: %v\n", err)
			continue
		}

		// Obfuscate the TunnelToken before storing
		if info.TunnelToken != "" {
			info.TunnelToken = obf.Encode(info.TunnelToken)
		}

		tunnels = append(tunnels, info)
		fmt.Printf("SUCCESS (ID: %s)\n", info.TunnelID)

		// Small delay to avoid aggressive rate limiting if necessary
		// Cloudflare API rate limits are usually high, but let's be safe.
		// time.Sleep(100 * time.Millisecond)
	}

	// Generate Go file
	if err := generateGoFile(*output, *packageName, tunnels); err != nil {
		log.Fatalf("Failed to generate Go file: %v", err)
	}

	fmt.Printf("\nDone! Successfully created %d/%d tunnels.\n", len(tunnels), *count)
	fmt.Printf("Go file generated at %s\n", *output)
	fmt.Printf("\nYou can now import and use:\n")
	fmt.Printf("  import \"github.com/kawai-network/veridium/pkg/tunnelkit\"\n")
	fmt.Printf("  tunnels := tunnelkit.GetTunnels()\n")
}

// generateGoFile creates a Go source file with tunnel configurations
func generateGoFile(outputPath, packageName string, tunnels []*tunnelkit.TunnelInfo) error {
	var sb strings.Builder

	// File header
	sb.WriteString("// Code generated by bulk-tunnels. DO NOT EDIT.\n")
	sb.WriteString(fmt.Sprintf("// Generated at: %s\n\n", time.Now().Format(time.RFC3339)))
	sb.WriteString(fmt.Sprintf("package %s\n\n", packageName))

	// Import obfuscator
	sb.WriteString("import (\n")
	sb.WriteString("\t\"github.com/kawai-network/x/env\"\n")
	sb.WriteString(")\n\n")

	// Tunnel data as constants
	sb.WriteString("// Tunnel configurations (tokens are obfuscated)\n")
	sb.WriteString("var tunnelConfigs = []struct {\n")
	sb.WriteString("\tTunnelID    string\n")
	sb.WriteString("\tTunnelToken string // Obfuscated\n")
	sb.WriteString("\tHostname    string\n")
	sb.WriteString("\tPublicURL   string\n")
	sb.WriteString("}{\n")

	// Add each tunnel
	for _, tunnel := range tunnels {
		sb.WriteString("\t{\n")
		sb.WriteString(fmt.Sprintf("\t\tTunnelID:    %q,\n", tunnel.TunnelID))
		sb.WriteString(fmt.Sprintf("\t\tTunnelToken: %q,\n", tunnel.TunnelToken))
		sb.WriteString(fmt.Sprintf("\t\tHostname:    %q,\n", tunnel.Hostname))
		sb.WriteString(fmt.Sprintf("\t\tPublicURL:   %q,\n", tunnel.PublicURL))
		sb.WriteString("\t},\n")
	}

	sb.WriteString("}\n\n")

	// GetTunnels function
	sb.WriteString("// GetTunnels returns all tunnel configurations with decoded tokens\n")
	sb.WriteString("func GetTunnels() []*TunnelInfo {\n")
	sb.WriteString("\tobf := env.New()\n")
	sb.WriteString("\ttunnels := make([]*TunnelInfo, len(tunnelConfigs))\n")
	sb.WriteString("\tfor i, cfg := range tunnelConfigs {\n")
	sb.WriteString("\t\tdecodedToken, _ := obf.Decode(cfg.TunnelToken)\n")
	sb.WriteString("\t\ttunnels[i] = &TunnelInfo{\n")
	sb.WriteString("\t\t\tTunnelID:    cfg.TunnelID,\n")
	sb.WriteString("\t\t\tTunnelToken: decodedToken,\n")
	sb.WriteString("\t\t\tHostname:    cfg.Hostname,\n")
	sb.WriteString("\t\t\tPublicURL:   cfg.PublicURL,\n")
	sb.WriteString("\t\t}\n")
	sb.WriteString("\t}\n")
	sb.WriteString("\treturn tunnels\n")
	sb.WriteString("}\n\n")

	// GetTunnelByID function
	sb.WriteString("// GetTunnelByID returns a tunnel by its ID with decoded token\n")
	sb.WriteString("func GetTunnelByID(tunnelID string) *TunnelInfo {\n")
	sb.WriteString("\tobf := env.New()\n")
	sb.WriteString("\tfor _, cfg := range tunnelConfigs {\n")
	sb.WriteString("\t\tif cfg.TunnelID == tunnelID {\n")
	sb.WriteString("\t\t\tdecodedToken, _ := obf.Decode(cfg.TunnelToken)\n")
	sb.WriteString("\t\t\treturn &TunnelInfo{\n")
	sb.WriteString("\t\t\t\tTunnelID:    cfg.TunnelID,\n")
	sb.WriteString("\t\t\t\tTunnelToken: decodedToken,\n")
	sb.WriteString("\t\t\t\tHostname:    cfg.Hostname,\n")
	sb.WriteString("\t\t\t\tPublicURL:   cfg.PublicURL,\n")
	sb.WriteString("\t\t\t}\n")
	sb.WriteString("\t\t}\n")
	sb.WriteString("\t}\n")
	sb.WriteString("\treturn nil\n")
	sb.WriteString("}\n\n")

	// GetTunnelByHostname function
	sb.WriteString("// GetTunnelByHostname returns a tunnel by its hostname with decoded token\n")
	sb.WriteString("func GetTunnelByHostname(hostname string) *TunnelInfo {\n")
	sb.WriteString("\tobf := env.New()\n")
	sb.WriteString("\tfor _, cfg := range tunnelConfigs {\n")
	sb.WriteString("\t\tif cfg.Hostname == hostname {\n")
	sb.WriteString("\t\t\tdecodedToken, _ := obf.Decode(cfg.TunnelToken)\n")
	sb.WriteString("\t\t\treturn &TunnelInfo{\n")
	sb.WriteString("\t\t\t\tTunnelID:    cfg.TunnelID,\n")
	sb.WriteString("\t\t\t\tTunnelToken: decodedToken,\n")
	sb.WriteString("\t\t\t\tHostname:    cfg.Hostname,\n")
	sb.WriteString("\t\t\t\tPublicURL:   cfg.PublicURL,\n")
	sb.WriteString("\t\t\t}\n")
	sb.WriteString("\t\t}\n")
	sb.WriteString("\t}\n")
	sb.WriteString("\treturn nil\n")
	sb.WriteString("}\n")

	// Write to file
	return os.WriteFile(outputPath, []byte(sb.String()), 0644)
}
