// NewAgentChatService creates a new agent-based chat service
// Set threadService to nil to disable thread management
func NewAgentChatService(
	app *application.App,
	db *database.Service,
	libService *llama.LibraryService,
	kbService *KnowledgeBaseService,
	vectorSearch *VectorSearchService,
	threadService *ThreadManagementService,
) *AgentChatService {
	ragWorkflow := NewRAGWorkflow(kbService)

	// Initialize Yzma Tool Registry & Register Builtin Tools
	// Pass SQL DB for tools that need database access (e.g., image describe)
	toolRegistry := tools.NewToolRegistry()
	if err := yzmabuiltin.RegisterAllWithDB(toolRegistry, db.DB()); err != nil {
		log.Printf("⚠️  Warning: Failed to register yzma builtin tools: %v", err)
	} else {
		log.Printf("✅ AgentChatService: Yzma builtin tools registered")
	}

	// Create Yzma model with tool registry (used for all LLM operations)
	yzmaModel := llama.NewLlamaYzmaModel(libService, toolRegistry)

	// Auto-detect utility models for lightweight tasks
	titleModelPath := detectTitleGenerationModel(libService)
	summaryModelPath := detectSummaryGenerationModel(libService)

	// Create LLM provider adapter (wraps yzmaModel for interface compatibility)
	llmGenerator := NewLlamaProviderAdapter(yzmaModel)

	// Build TaskRouter with default dev config
	// This routes different tasks to appropriate providers
	devConfig := llm.GetDefaultDevConfig()
	taskRouter := llm.BuildTaskRouter(devConfig, toolRegistry, llmGenerator)

	service := &AgentChatService{
		app:              app,
		db:               db,
		libService:       libService,
		yzmaModel:        yzmaModel,
		llmGenerator:     llmGenerator,
		taskRouter:       taskRouter,
		kbService:        kbService,
		ragWorkflow:      ragWorkflow,
		vectorSearch:     vectorSearch,
		toolRegistry:     toolRegistry,
		threadService:    threadService,
		reasoningConfig:  DefaultReasoningConfig(), // Default: disabled (non-reasoning)
		titleModelPath:   titleModelPath,
		summaryModelPath: summaryModelPath,
		sessions:         make(map[string]*AgentSession),
	}

	if titleModelPath != "" {
		log.Printf("📝 Auto-detected title model: %s", filepath.Base(titleModelPath))
	} else {
		log.Printf("📝 No dedicated title model found, will use main chat model")
	}

	if summaryModelPath != "" {
		log.Printf("📋 Auto-detected summary model: %s", filepath.Base(summaryModelPath))
	} else {
		log.Printf("📋 No dedicated summary model found, will use main chat model")
	}

	return service
}

