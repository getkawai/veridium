// detectTitleGenerationModel finds the smallest, fastest model for title generation
// Prefers utility models (Llama 3.2 1B/3B) which are optimized for quick tasks
// Reuses detectSummaryGenerationModel since both tasks need the same type of model
func detectTitleGenerationModel(libService *llama.LibraryService) string {
	return detectSummaryGenerationModel(libService)
}

// detectSummaryGenerationModel finds optimal model for summarization
// Strategy: Prefer small non-reasoning models (Llama 3.2 1B/3B, Mistral)
// These models are fast, don't generate <think> tags, and have good quality for utility tasks
func detectSummaryGenerationModel(libService *llama.LibraryService) string {
	models, err := libService.GetAvailableModels()
	if err != nil || len(models) == 0 {
		return ""
	}

	homeDir, err := os.UserHomeDir()
	if err != nil {
		return ""
	}
	modelsDir := filepath.Join(homeDir, ".llama-cpp", "models")

	var bestModel string
	var bestScore int

	for _, modelName := range models {
		modelPath := filepath.Join(modelsDir, modelName)
		nameLower := strings.ToLower(modelName)

		// Skip embedding models
		if strings.Contains(nameLower, "embedding") || strings.Contains(nameLower, "embed") {
			continue
		}

		// Get file size
		info, err := os.Stat(modelPath)
		if err != nil {
			continue
		}

		score := 0
		sizeMB := info.Size() / (1024 * 1024)

		// Prefer SMALL but not TOO small (1B-1.5B ideal for summary)
		// Summary needs slightly better quality than title
		if sizeMB >= 500 && sizeMB < 1000 {
			score += 100 // 1B models - BEST for summary (Llama 3.2 1B)
		} else if sizeMB >= 1000 && sizeMB < 2000 {
			score += 90 // 1.5B-3B models - good balance (Llama 3.2 3B)
		} else if sizeMB < 500 {
			score += 70 // 0.5B models - may be too simple
		} else if sizeMB < 4500 {
			score += 50 // 3B-7B models - slower, unnecessary (Mistral 7B)
		} else {
			score += 10 // 7B+ models - too slow for background task
		}

		// CRITICAL: Prefer non-reasoning models (NO <think> tags)
		if strings.Contains(nameLower, "llama-3.2-1b") || strings.Contains(nameLower, "llama_3.2_1b") {
			score += 100 // Llama 3.2 1B is BEST - fast, no think tags, good quality
		} else if strings.Contains(nameLower, "llama-3.2-3b") || strings.Contains(nameLower, "llama_3.2_3b") {
			score += 90 // Llama 3.2 3B is excellent - better quality
		} else if strings.Contains(nameLower, "llama") && !strings.Contains(nameLower, "3.2") {
			score += 70 // Other Llama models are okay
		} else if strings.Contains(nameLower, "mistral") {
			score += 60 // Mistral is decent
		} else if strings.Contains(nameLower, "gemma") {
			score += 50 // Gemma is okay
		} else if strings.Contains(nameLower, "phi") {
			score += 40 // Phi is acceptable
		} else if strings.Contains(nameLower, "qwen") {
			score -= 200 // AVOID Qwen - generates <think> tags in summary!
		} else if strings.Contains(nameLower, "deepseek") {
			score -= 150 // AVOID DeepSeek - reasoning model
		}

		// Prefer instruct/chat models
		if strings.Contains(nameLower, "instruct") || strings.Contains(nameLower, "chat") {
			score += 30
		}

		// Prefer Q4 quantization (good balance of quality & speed)
		if strings.Contains(nameLower, "q4") {
			score += 20
		}

		if score > bestScore {
			bestScore = score
			bestModel = modelPath
		}
	}

	return bestModel
}

// Chat processes a chat request and returns a response
func (s *AgentChatService) Chat(ctx context.Context, req ChatRequest) (*UIChatMessage, error) {
	// 1. Setup session, topic, and save user message using reusable helper
	// SetupSessionAndTopic handles:
	// - Get/create session
	// - Load history summary from DB
	// - Load thread messages
	// - Auto-create topic
	// - Add user message to session (in-memory)
	// - Save user message to DB
	setup, err := s.setupSessionAndTopic(ctx, req)
	if err != nil {
		return nil, fmt.Errorf("failed to setup session/topic: %w", err)
	}
	session := setup.Session
	currentTopicID := setup.TopicID

	// 2. Validate model for current reasoning mode and auto-switch if needed
	// Note: This is NOT in SetupSessionAndTopic because it's specific to Chat()
	if err := s.ValidateModelForReasoningMode(); err != nil {
		log.Printf("⚠️  Model mismatch detected: %v", err)
		log.Printf("🔄 Auto-switching to recommended model...")
		if switchErr := s.SwitchToRecommendedModel(); switchErr != nil {
			log.Printf("❌ Failed to switch model: %v", switchErr)
			log.Printf("⚠️  Continuing with current model, but expect suboptimal performance")
		} else {
			log.Printf("✅ Successfully switched to recommended model")
		}
	}

	// Messages for agent (native yzma format)
	messagesToAgent := session.Messages

	// Prepare messages with system prompt
	messagesWithSystem := s.prepareMessagesWithSystemPrompt(messagesToAgent, session)

	// Use pre-generated assistant message ID from frontend, or generate new one
	assistantMsgID := req.MessageAssistantID
	if assistantMsgID == "" {
		assistantMsgID = uuid.New().String()
	}

	// Get LLM provider with requested tools
	// Use TaskRouter if configured, otherwise fallback to llmGenerator
	var llmWithTools llm.Provider
	if s.taskRouter != nil {
		llmWithTools = s.taskRouter.ChatWithTools(session.ToolNames)
		if llmWithTools == nil {
			llmWithTools = s.llmGenerator.WithTools(session.ToolNames)
		}
	} else {
		llmWithTools = s.llmGenerator.WithTools(session.ToolNames)
	}

	// Run agent with LLM generator (streaming or non-streaming)
	var finalMessage string
	var toolCalls []types.ToolCall
	var usage *types.LLMResponse
	var toolMessages []types.Message

	if req.Stream && s.app != nil {
		// Use streaming with agent loop
		resp, tms, streamErr := s.generateWithStreaming(ctx, session, assistantMsgID, messagesWithSystem, llmWithTools)
		if streamErr != nil {
			return nil, fmt.Errorf("streaming generation failed: %w", streamErr)
		}
		finalMessage = resp.Content
		toolCalls = resp.ToolCalls
		toolMessages = tms
	} else {
		// Use agent loop (non-streaming)
		resp, tms, runErr := llmWithTools.RunAgentLoop(ctx, messagesWithSystem, 10)
		if runErr != nil {
			return nil, fmt.Errorf("agent execution failed: %w", runErr)
		}

		finalMessage = resp.Content
		toolCalls = resp.ToolCalls
		toolMessages = tms
		usage = resp
	}

	// Add tool messages to session (for history)
	session.Messages = append(session.Messages, toolMessages...)

	// Note: Think tag stripping removed - proper model selection should prevent think tags
	// If reasoning mode is disabled, use non-reasoning models (Llama 3.2)
	// If reasoning mode is enabled, use reasoning models with /no_think (Qwen3)

	// Add assistant response to session
	if len(toolCalls) > 0 {
		session.Messages = append(session.Messages, types.NewToolCallMessage(toolCalls))
	} else {
		session.Messages = append(session.Messages, types.NewAssistantMessage(finalMessage))
	}

	// Monitor context usage and provide warnings/recommendations
	if usage != nil && usage.TotalTokens > 0 {
		contextSize := 16384 // Current context window size
		contextUsage := float64(usage.TotalTokens) / float64(contextSize)
		turnCount := len(session.Messages) / 2 // Approximate turn count (user + assistant pairs)

		log.Printf("📊 Context usage: %d/%d tokens (%.1f%%) after %d turns",
			usage.TotalTokens, contextSize, contextUsage*100, turnCount)

		// Warning at 50% usage
		if contextUsage > 0.5 && contextUsage <= 0.8 {
			log.Printf("⚠️  Context usage > 50%%. Consider switching to more efficient reasoning mode.")
			if s.reasoningConfig.Mode != ReasoningDisabled {
				log.Printf("💡 Recommendation: Switch to ReasoningDisabled (Llama 3.2) for longer conversations")
			}
		}

		// Critical warning at 80% usage
		if contextUsage > 0.8 {
			log.Printf("🚨 Context usage > 80%% Conversation may become unstable.")
			log.Printf("💡 Recommendations:")
			log.Printf("   1. Switch to ReasoningDisabled mode")
			log.Printf("   2. Summarize and truncate old messages")
			log.Printf("   3. Start a new conversation")
		}
	}

	// Phase 4: Generate topic title after first response
	// If we already created a placeholder topic, update it with LLM-generated title
	// We allow up to 4 messages (2 turns) to retry title generation if it failed or was skipped
	if len(session.Messages) >= 2 && len(session.Messages) <= 4 {
		log.Printf("📝 Checking if topic title needs update (msgs: %d, topic: %s)", len(session.Messages), currentTopicID)

		if currentTopicID != "" {
			// Update existing topic with LLM-generated title
			// This runs in background, so it won't block response
			err := s.updateTopicTitle(ctx, currentTopicID, session.UserID, session.Messages)
			if err != nil {
				log.Printf("⚠️  Warning: Failed to trigger topic title update: %v", err)
			} else {
				log.Printf("📝 Triggered background title update for topic %s", currentTopicID)
			}
		} else {
			// Topic was not created before - create it now with the actual conversation
			topicID, err := s.createTopicForSessionWithTitle(ctx, session.SessionID, session.UserID, session.Messages)
			if err != nil {
				log.Printf("⚠️  Warning: Failed to create topic with title: %v", err)
			} else {
				currentTopicID = topicID
				session.TopicID = topicID
				log.Printf("📝 Created topic with auto-generated title: %s", topicID)
			}
		}
	}

	// Save assistant message to DB with topic and thread IDs
	var assistantMsgForDB types.Message
	if len(toolCalls) > 0 {
		assistantMsgForDB = types.NewToolCallMessage(toolCalls)
	} else {
		assistantMsgForDB = types.NewAssistantMessage(finalMessage)
	}
	savedMsgID, err := s.saveYzmaMessageToDBWithID(ctx, assistantMsgForDB, session.SessionID, session.UserID, currentTopicID, req.ThreadID, assistantMsgID)
	if err != nil {
		log.Printf("⚠️  Warning: Failed to save assistant message to DB: %v", err)
		// Use the generated ID as fallback
		savedMsgID = assistantMsgID
	} else {
		log.Printf("💾 Saved assistant message: %s (topic: %s, thread: %s)", savedMsgID, currentTopicID, req.ThreadID)
		// Update assistantMsgID with the actual DB ID (should be the same as we passed it in)
		assistantMsgID = savedMsgID
	}

	// ✅ AUTO-TRIGGER SUMMARY (NON-BLOCKING)
	// This happens AFTER response is sent to user, so no blocking
	// Summary generation runs in background goroutine
	if currentTopicID != "" {
		go func() {
			bgCtx := context.Background()
			// First: Try initial summary creation (if no summary exists)
			s.autoSummarizeIfNeeded(bgCtx, session, currentTopicID, session.UserID)

			// Second: Try incremental summary update (if summary exists and enough new messages)
			s.incrementalSummarizeIfNeeded(bgCtx, session, currentTopicID, session.UserID)
		}()
	}

	// Emit streaming complete event (if streaming enabled)
	if req.Stream && s.app != nil {
		s.app.Event.Emit("chat:stream", map[string]interface{}{
			"type":       "complete",
			"session_id": session.SessionID,
			"message_id": assistantMsgID,
			"content":    finalMessage,
			"topic_id":   currentTopicID,
			"thread_id":  req.ThreadID,
		})
	}

	// Update session timestamp in DB
	if err := s.updateSessionTimestamp(ctx, session.SessionID, session.UserID); err != nil {
		log.Printf("⚠️  Warning: Failed to update session timestamp: %v", err)
	}

	// Phase 4: Return with all relevant IDs
	now := time.Now().UnixMilli()

	// Convert yzma ToolCalls to UI format
	var uiTools []ChatToolPayload
	if len(toolCalls) > 0 {
		uiTools = make([]ChatToolPayload, len(toolCalls))
		for i, tc := range toolCalls {
			argsJSON, _ := json.Marshal(tc.Function.Arguments)
			uiTools[i] = ChatToolPayload{
				ID:         fmt.Sprintf("%s_%d", assistantMsgID, i),
				APIName:    tc.Function.Name,
				Identifier: tc.Function.Name,
				Arguments:  string(argsJSON),
				Type:       tc.Type,
			}
		}
	}

	// Convert usage to UI format
	var uiUsage *ModelUsage
	if usage != nil {
		uiUsage = &ModelUsage{
			TotalInputTokens:  usage.PromptTokens,
			TotalOutputTokens: usage.CompletionTokens,
			TotalTokens:       usage.TotalTokens,
		}
	}

	return &UIChatMessage{
		ID:        assistantMsgID,
		SessionID: session.SessionID,
		TopicID:   currentTopicID,
		ThreadID:  req.ThreadID,
		Content:   finalMessage,
		Role:      UIMessageRoleAssistant,
		Tools:     uiTools,
		Usage:     uiUsage,
		Meta:      MetaData{},
		CreatedAt: now,
		UpdatedAt: now,
	}, nil
}

// getOrCreateSession gets an existing session or creates a new one with DB persistence
func (s *AgentChatService) getOrCreateSession(ctx context.Context, req ChatRequest) (*AgentSession, error) {
	s.sessionsMutex.Lock()
	defer s.sessionsMutex.Unlock()

	// 1. Check in-memory cache first
	if session, exists := s.sessions[req.SessionID]; exists {
		log.Printf("♻️  Reusing cached session: %s", req.SessionID)
		// Update tool names if changed
		session.ToolNames = s.collectToolNames(ctx, req)
		return session, nil
	}

	// 2. Try to load from database
	dbSession, dbMessages, err := s.loadSessionFromDB(ctx, req.SessionID, req.UserID)
	if err == nil {
		// Session exists in DB - reconstruct from history
		log.Printf("📂 Loading session from DB: %s (%d messages)", req.SessionID, len(dbMessages))

		// Convert DB messages to message format
		yzmaMessages := make([]types.Message, 0, len(dbMessages))
		for _, dbMsg := range dbMessages {
			if msg, ok := convertDBMessageToYzma(&dbMsg); ok {
				yzmaMessages = append(yzmaMessages, msg)
			}
		}

		// Collect tool names for yzma
		toolNames := s.collectToolNames(ctx, req)

		// Create session with history
		session := &AgentSession{
			SessionID:       req.SessionID,
			UserID:          req.UserID,
			Messages:        yzmaMessages,
			KnowledgeBaseID: req.KnowledgeBaseID,
			ToolNames:       toolNames,
			Context:         req.Context,
			CreatedAt:       dbSession.CreatedAt,
			UpdatedAt:       dbSession.UpdatedAt,
			DBSession:       dbSession,
		}

		s.sessions[req.SessionID] = session

		log.Printf("✅ Reconstructed session from DB: %s (KB: %s, History: %d msgs, Tools: %v)",
			req.SessionID, req.KnowledgeBaseID, len(yzmaMessages), toolNames)

		return session, nil
	}

	// 3. Session doesn't exist - create new one
	log.Printf("🆕 Creating new session: %s", req.SessionID)

	// Create session in DB (handle race condition with UNIQUE constraint)
	dbSession, err = s.createSessionInDB(ctx, req.SessionID, req.UserID, req.KnowledgeBaseID)
	if err != nil {
		// Check if error is due to UNIQUE constraint (race condition)
		// Must NOT be FOREIGN KEY constraint (which is a real error, not race condition)
		errStr := err.Error()
		isUniqueConstraint := strings.Contains(errStr, "UNIQUE constraint failed")
		isForeignKey := strings.Contains(errStr, "FOREIGN KEY")
		if isUniqueConstraint && !isForeignKey {
			// Another process created the session - retry loading with exponential backoff
			log.Printf("⚠️  Race condition detected, retrying load from DB...")

			var dbMessages []db.Message
			maxRetries := 3
			for i := 0; i < maxRetries; i++ {
				// Wait a bit for the other process to commit
				time.Sleep(time.Duration(10*(i+1)) * time.Millisecond)

				dbSession, dbMessages, err = s.loadSessionFromDB(ctx, req.SessionID, req.UserID)
				if err == nil {
					log.Printf("✅ Successfully loaded session after %d retries", i+1)
					break
				}

				if i < maxRetries-1 {
					log.Printf("⏳ Retry %d/%d: session not yet available, waiting...", i+1, maxRetries)
				}
			}

			if err != nil {
				return nil, fmt.Errorf("failed to load session after race condition (tried %d times): %w", maxRetries, err)
			}

			// Convert DB messages to message format
			yzmaMessages := make([]types.Message, 0, len(dbMessages))
			for _, dbMsg := range dbMessages {
				if msg, ok := convertDBMessageToYzma(&dbMsg); ok {
					yzmaMessages = append(yzmaMessages, msg)
				}
			}

			// Collect tool names
			toolNames := s.collectToolNames(ctx, req)

			// Create session with history
			session := &AgentSession{
				SessionID:       req.SessionID,
				UserID:          req.UserID,
				Messages:        yzmaMessages,
				KnowledgeBaseID: req.KnowledgeBaseID,
				ToolNames:       toolNames,
				Context:         req.Context,
				CreatedAt:       dbSession.CreatedAt,
				UpdatedAt:       dbSession.UpdatedAt,
				DBSession:       dbSession,
			}

			s.sessions[req.SessionID] = session

			log.Printf("✅ Recovered from race condition: %s", req.SessionID)
			return session, nil
		}

		// Other error - fail
		return nil, fmt.Errorf("failed to create session in DB: %w", err)
	}

	// Collect tool names for yzma
	toolNames := s.collectToolNames(ctx, req)

	// Create in-memory session
	session := &AgentSession{
		SessionID:       req.SessionID,
		UserID:          req.UserID,
		Messages:        make([]types.Message, 0),
		KnowledgeBaseID: req.KnowledgeBaseID,
		ToolNames:       toolNames,
		Context:         req.Context,
		CreatedAt:       dbSession.CreatedAt,
		UpdatedAt:       dbSession.UpdatedAt,
		DBSession:       dbSession,
	}

	s.sessions[req.SessionID] = session

	log.Printf("✅ Created new session: %s (KB: %s, Tools: %v)",
		req.SessionID, req.KnowledgeBaseID, toolNames)

	return session, nil
}

// collectToolNames collects tool names for the session based on request
// If no tools specified, returns nil which means "use all registered tools"
func (s *AgentChatService) collectToolNames(ctx context.Context, req ChatRequest) []string {
	var toolNames []string

	// Add KB search tool if KB is specified
	if req.KnowledgeBaseID != "" {
		// Register KB tool to registry if not exists
		if err := s.registerKBSearchTool(ctx, req.KnowledgeBaseID, req.UserID); err != nil {
			log.Printf("⚠️  Warning: Failed to register KB tool: %v", err)
		} else {
			kb, err := s.kbService.GetKnowledgeBase(ctx, req.KnowledgeBaseID, req.UserID)
			if err == nil {
				toolNames = append(toolNames, fmt.Sprintf("search_%s", kb.Name))
			}
		}
	}

	// Add requested tools
	toolNames = append(toolNames, req.Tools...)

	// If no tools specified, return nil to use all registered tools
	// This allows LLM to use any available tool including lobe-image-describe
	if len(toolNames) == 0 {
		return nil
	}

	return toolNames
}

// prepareMessagesWithSystemPrompt prepares messages with system prompt for yzma
func (s *AgentChatService) prepareMessagesWithSystemPrompt(messages []types.Message, session *AgentSession) []types.Message {
	// Build base instruction
	baseInstruction := "You are a helpful AI assistant. "

	// Inject summary if exists
	if session.Context != nil {
		if historySummary, ok := session.Context["history_summary"].(string); ok && historySummary != "" {
			summaryContext := fmt.Sprintf(`

<chat_history_summary>
<docstring>Previous conversation summary (older messages have been compressed):</docstring>
<summary>%s</summary>
</chat_history_summary>

`, historySummary)
			baseInstruction += summaryContext
			log.Printf("📋 Injected history summary into system prompt (%d chars)", len(historySummary))
		}
	}

	if session.KnowledgeBaseID != "" {
		baseInstruction += "You have access to a knowledge base. Use the search tool to find relevant information before answering questions. "
	}

	// Apply reasoning mode to instruction
	instruction := s.reasoningConfig.GetSystemPrompt(baseInstruction)

	log.Printf("🧠 Preparing messages with reasoning mode: %s", s.reasoningConfig.Mode)
	log.Printf("📝 System prompt length: %d chars", len(instruction))

	// Check if messages already have system prompt
	if len(messages) > 0 && messages[0].GetRole() == "system" {
		// Update existing system prompt
		result := make([]types.Message, len(messages))
		copy(result, messages)
		result[0] = types.NewSystemMessage(instruction)
		return result
	}

	// Prepend system prompt
	result := make([]types.Message, 0, len(messages)+1)
	result = append(result, types.NewSystemMessage(instruction))
	result = append(result, messages...)
	return result
}

// generateWithStreaming generates response with streaming using llm.Provider interface
func (s *AgentChatService) generateWithStreaming(ctx context.Context, session *AgentSession, messageID string, messages []types.Message, provider llm.Provider) (*types.LLMResponse, []types.Message, error) {
	// Emit start event
	if s.app != nil {
		s.app.Event.Emit("chat:stream", map[string]interface{}{
			"type":       "start",
			"session_id": session.SessionID,
			"message_id": messageID,
		})
	}

	var fullContent strings.Builder
	var buffer strings.Builder // Buffer untuk detect tool tags

	// Streaming callback with tool tag filtering
	callback := func(token string, isLast bool) {
		if token != "" {
			fullContent.WriteString(token)
			buffer.WriteString(token)

			// Check if we're inside a tool_call tag
			bufferStr := buffer.String()

			// If buffer contains complete tool_call tags, remove them from display
			if strings.Contains(bufferStr, "</tool_call>") {
				// Find all complete tool_call blocks
				for strings.Contains(bufferStr, "<tool_call>") && strings.Contains(bufferStr, "</tool_call>") {
					startIdx := strings.Index(bufferStr, "<tool_call>")
					endIdx := strings.Index(bufferStr, "</tool_call>")

					if startIdx < endIdx {
						// Remove the tool_call block
						bufferStr = bufferStr[:startIdx] + bufferStr[endIdx+len("</tool_call>"):]
					} else {
						break
					}
				}
				buffer.Reset()
				buffer.WriteString(bufferStr)
			}

			// Only emit content that's not inside tool tags
			displayContent := bufferStr
			if strings.Contains(displayContent, "<tool_call>") && !strings.Contains(displayContent, "</tool_call>") {
				// We're in the middle of a tool_call tag, only show content before it
				displayContent = displayContent[:strings.Index(displayContent, "<tool_call>")]
			}

			// Emit chunk event with filtered content
			if s.app != nil {
				s.app.Event.Emit("chat:stream", map[string]interface{}{
					"type":         "chunk",
					"session_id":   session.SessionID,
					"message_id":   messageID,
					"content":      token,
					"full_content": strings.TrimSpace(displayContent),
				})
			}
		}

		if isLast {
			// Clean final content from tool tags
			finalContent := fullContent.String()
			for strings.Contains(finalContent, "<tool_call>") {
				start := strings.Index(finalContent, "<tool_call>")
				end := strings.Index(finalContent, "</tool_call>")
				if start != -1 && end != -1 {
					finalContent = finalContent[:start] + finalContent[end+len("</tool_call>"):]
				} else {
					break
				}
			}
			finalContent = strings.TrimSpace(finalContent)

			// Emit complete event
			if s.app != nil {
				s.app.Event.Emit("chat:stream", map[string]interface{}{
					"type":       "complete",
					"session_id": session.SessionID,
					"message_id": messageID,
					"content":    finalContent,
				})
			}
		}
	}

	// Run agent loop with streaming (no tool callback for legacy method)
	resp, toolMessages, err := provider.RunAgentLoopWithStreaming(ctx, messages, 10, callback, nil)
	if err != nil {
		return nil, nil, err
	}

	// Emit tool calling event if tools were used
	if len(resp.ToolCalls) > 0 && s.app != nil {
		// Convert tool calls to frontend format
		frontendTools := make([]map[string]interface{}, len(resp.ToolCalls))
		for i, tc := range resp.ToolCalls {
			// Generate unique ID for each tool call
			toolCallID := fmt.Sprintf("%s_%d", messageID, i)

			frontendTools[i] = map[string]interface{}{
				"id":         toolCallID,
				"identifier": tc.Function.Name,
				"apiName":    tc.Function.Name,
				"arguments":  tc.Function.Arguments,
				"type":       tc.Type,
			}
		}

		s.app.Event.Emit("chat:stream", map[string]interface{}{
			"type":       "tool_calling",
			"session_id": session.SessionID,
			"message_id": messageID,
			"tools":      frontendTools,
		})

		log.Printf("🔧 Emitted tool_calling event with %d tools", len(frontendTools))
	}

	return resp, toolMessages, nil
}

// registerKBSearchTool registers a knowledge base search tool to the yzma registry
func (s *AgentChatService) registerKBSearchTool(ctx context.Context, kbID, userID string) error {
	kb, err := s.kbService.GetKnowledgeBase(ctx, kbID, userID)
	if err != nil {
		return err
	}

	toolName := fmt.Sprintf("search_%s", kb.Name)

	// Check if already registered
	if _, exists := s.toolRegistry.Get(toolName); exists {
		return nil // Already registered
	}

	// Create yzma tool
	kbService := s.kbService
	tool := &types.Tool{
		Type: "function",
		Function: types.ToolFunction{
			Name:        toolName,
			Description: fmt.Sprintf("Search the %s knowledge base for relevant information", kb.Name),
			Parameters: map[string]interface{}{
				"type": "object",
				"properties": map[string]interface{}{
					"query": map[string]interface{}{
						"type":        "string",
						"description": "The search query to find relevant information",
					},
					"top_k": map[string]interface{}{
						"type":        "integer",
						"description": "Number of results to return (default: 5)",
					},
				},
				"required": []string{"query"},
			},
		},
		Executor: func(execCtx context.Context, args map[string]string) (string, error) {
			query := args["query"]
			if query == "" {
				return "", fmt.Errorf("query parameter is required")
			}

			topK := 5
			if topKStr, ok := args["top_k"]; ok && topKStr != "" {
				if n, err := fmt.Sscanf(topKStr, "%d", &topK); err != nil || n != 1 {
					topK = 5
				}
			}

			// Query knowledge base
			docs, err := kbService.QueryKnowledgeBase(execCtx, kbID, query, topK, userID)
			if err != nil {
				return "", fmt.Errorf("KB search failed: %w", err)
			}

			// Format results
			result := map[string]interface{}{
				"found": len(docs),
				"results": func() []map[string]interface{} {
					results := make([]map[string]interface{}, len(docs))
					for i, doc := range docs {
						results[i] = map[string]interface{}{
							"content":  doc.Content,
							"metadata": doc.Metadata,
						}
					}
					return results
				}(),
			}

			resultJSON, err := json.Marshal(result)
			if err != nil {
				return "", fmt.Errorf("failed to marshal result: %w", err)
			}

			return string(resultJSON), nil
		},
		Enabled: true,
	}

	return s.toolRegistry.Register(tool)
}

